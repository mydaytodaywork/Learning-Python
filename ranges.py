#range is another kind of immutable sequence type.
#to create ranges
#Ranges are also generators.

# print the integers from 0 to 9
print(list(range(10)))

# print the integers from 1 to 10
print(list(range(1, 11)))

# print the odd integers from 1 to 10 with step size 2
print(list(range(1, 11, 2)))
